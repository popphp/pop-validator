class HasOneDateTimeThatEquals extends HasOneThatEquals
{

    /**
     * Method to evaluate the validator
     *
     * @param  mixed $input
     * @throws Exception
     * @return bool
     */
    public function evaluate(mixed $input = null, bool $isDateTime = true): bool
    {
        return parent::evaluate($input, $isDateTime);
    }

}
